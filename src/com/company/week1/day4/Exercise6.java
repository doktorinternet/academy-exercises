package com.company.week1.day4;

public class Exercise6 {

    public static void printColorStrings(char  color){
        String print = "";
        switch(color) {
            case 'a':
                System.out.println("black");
                break;
            case 'b' :
                print = "white";
            case 'w':
                print = "white";
            case 'W':
                print = "white";
            case 'v':
                print = "white";
                System.out.println(print);
                break;
            case 'c':
                System.out.println("red");
                break;
            case 'd':
                System.out.println("green");
                break;
        }
    }

}
